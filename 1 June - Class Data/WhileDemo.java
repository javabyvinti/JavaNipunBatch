package com.vinti.javabasic;

public class WhileDemo {

	public static void main(String[] args) {
	
		// 1 to 10 
		
		int num = 1;	// initialization
				// 1<=10
		while(num<=10) {	// condition
			System.out.println(num);
			num = num + 1;	// increment
			// 1 + 1
		}
		

	}

}
